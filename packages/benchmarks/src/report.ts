/**
 * Markdown report writer — Slice 3-XR-Compact P1 skeleton.
 *
 * Renders FixtureResult[] grouped by strategy as a side-by-side comparison
 * table plus per-fixture breakdown. Designed to drop directly into
 * `.agents/progress/compact-bench-{date}.md` as a ledger entry.
 */

import type { FixtureResult, StrategyId } from "./fixture.js";

export interface ReportInput {
	readonly date: string;
	readonly fixtureCount: number;
	readonly strategiesUnderTest: readonly StrategyId[];
	readonly results: readonly FixtureResult[];
	readonly judgeProfile: "glm-only" | "ensemble" | "none";
	readonly notes?: string;
}

interface StrategyAggregate {
	readonly strategy: StrategyId;
	readonly taskAccuracy: number;
	readonly factRecall: number;
	readonly latencyP50Ms: number;
	readonly latencyP99Ms: number;
	readonly compactionLatencyMs: number;
	readonly totalTokens: number;
	readonly driftScore: number;
	readonly fixtures: number;
	readonly errors: number;
}

function aggregateByStrategy(results: readonly FixtureResult[]): StrategyAggregate[] {
	const byStrategy = new Map<StrategyId, FixtureResult[]>();
	for (const r of results) {
		const arr = byStrategy.get(r.strategy) ?? [];
		arr.push(r);
		byStrategy.set(r.strategy, arr);
	}
	const out: StrategyAggregate[] = [];
	for (const [strategy, group] of byStrategy.entries()) {
		const n = group.length;
		if (n === 0) continue;
		const mean = (key: keyof FixtureResult): number => {
			const sum = group.reduce((acc, r) => acc + (r[key] as number), 0);
			return sum / n;
		};
		out.push({
			strategy,
			taskAccuracy: mean("taskAccuracy"),
			factRecall: mean("factRecall"),
			latencyP50Ms: mean("latencyP50Ms"),
			latencyP99Ms: mean("latencyP99Ms"),
			compactionLatencyMs: mean("compactionLatencyMs"),
			totalTokens: Math.round(mean("totalTokens")),
			driftScore: mean("driftScore"),
			fixtures: n,
			errors: group.reduce((acc, r) => acc + r.errors.length, 0),
		});
	}
	return out;
}

function fmt(n: number, digits = 3): string {
	if (Number.isNaN(n) || !Number.isFinite(n)) return "—";
	return n.toFixed(digits);
}

export function renderReport(input: ReportInput): string {
	const agg = aggregateByStrategy(input.results);
	const lines: string[] = [];
	lines.push(`# Compaction Benchmark — ${input.date}`);
	lines.push("");
	lines.push(`- **Fixtures**: ${input.fixtureCount}`);
	lines.push(`- **Strategies under test**: ${input.strategiesUnderTest.join(", ")}`);
	lines.push(`- **Judge profile**: ${input.judgeProfile}`);
	if (input.notes) {
		lines.push(`- **Notes**: ${input.notes}`);
	}
	lines.push("");

	lines.push("## Aggregate (mean across fixtures)");
	lines.push("");
	lines.push(
		"| Strategy | Task-accuracy | Fact-recall | Latency p50 | Latency p99 | Compact ms | Total tokens | Drift | Fixtures | Errors |",
	);
	lines.push(
		"|---|---:|---:|---:|---:|---:|---:|---:|---:|---:|",
	);
	for (const a of agg) {
		lines.push(
			`| \`${a.strategy}\` | ${fmt(a.taskAccuracy)} | ${fmt(a.factRecall)} | ${fmt(a.latencyP50Ms, 0)}ms | ${fmt(a.latencyP99Ms, 0)}ms | ${fmt(a.compactionLatencyMs, 0)}ms | ${a.totalTokens} | ${fmt(a.driftScore)} | ${a.fixtures} | ${a.errors} |`,
		);
	}
	lines.push("");

	lines.push("## Per-fixture breakdown");
	lines.push("");
	lines.push(
		"| Fixture | Strategy | Task | Recall | p50 | p99 | Compact | Tokens | Drift | Errors |",
	);
	lines.push("|---|---|---:|---:|---:|---:|---:|---:|---:|---:|");
	for (const r of input.results) {
		const errSummary = r.errors.length === 0 ? "—" : `${r.errors.length}`;
		lines.push(
			`| \`${r.fixtureId}\` | \`${r.strategy}\` | ${fmt(r.taskAccuracy)} | ${fmt(r.factRecall)} | ${fmt(r.latencyP50Ms, 0)}ms | ${fmt(r.latencyP99Ms, 0)}ms | ${fmt(r.compactionLatencyMs, 0)}ms | ${r.totalTokens} | ${fmt(r.driftScore)} | ${errSummary} |`,
		);
	}
	lines.push("");

	lines.push("## Recommendation");
	lines.push("");
	lines.push("> _Generated by `pnpm bench:compact`. See `docs/compaction-survey.md` for strategy definitions and `nextain/naia-agent#47` for the slice umbrella._");
	lines.push("");

	return lines.join("\n");
}
